    it("calculates the box for a geohash string.", () => {
      const hash = "9q9j";
      const box = m.Geohashes.hashBox(hash);
      expect(box.south).toBeCloseTo(37.441406);
      expect(box.west).toBeCloseTo(-122.34375);
      expect(box.north).toBeCloseTo(37.617188);
      expect(box.east).toBeCloseTo(-121.99219);
    });

